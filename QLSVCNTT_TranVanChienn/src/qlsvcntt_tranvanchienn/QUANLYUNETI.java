    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            Connection kn = ketnoi.KetnoiSQL();
            String ma = txtMa.getText();
            String ten = txtTen.getText();
            String khoa = txtKhoa.getText();
            String nam = txtNam.getText();
            String them = "INSERT INTO truonghoc VALUES('" + ma + "', '" + ten + "', '" + khoa + "', '" + nam + "')";
            Statement stm = kn.createStatement();
            stm.executeUpdate(them);
            showData();
        } catch (SQLException ex) {
            Logger.getLogger(QUANLYUNETI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed


    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        int row = tbl.getSelectedRow();
        txtMa.setText(tbl.getValueAt(row, 0).toString());
        txtTen.setText(tbl.getValueAt(row, 1).toString());
        txtKhoa.setText(tbl.getValueAt(row, 2).toString());
        txtNam.setText(tbl.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblMouseClicked

