    public void showData() {
        try {
            Connection kn = ketnoi.KetnoiSQL();
            String hienthi = "SELECT * FROM truonghoc";
            Statement stm = kn.createStatement();
            ResultSet rs = stm.executeQuery(hienthi);
            DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Object obj[] = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getInt(4),};
                dtm.addRow(obj);
                tbl.setModel(dtm);
            }
        } catch (SQLException ex) {
            Logger.getLogger(QUANLYUNETI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

