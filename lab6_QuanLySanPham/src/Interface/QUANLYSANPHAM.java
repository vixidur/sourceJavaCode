    private boolean CheckInput() {
        String masp = txtMaSanPham.getText().trim();
        String tensp = txtTenSanPham.getText().trim();
        String gia = txtGiaBan.getText().trim();
        String loaisp = cbLoaiSP.getSelectedItem().toString().trim();

        if (masp.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập mã số lớp", "Chưa  nhập mã số lớp", JOptionPane.WARNING_MESSAGE);
            txtMaSanPham.requestFocus();
            return false;
        }
        if (tensp.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên lớp", "Chưa nhập tên lớp", JOptionPane.WARNING_MESSAGE);
            txtTenSanPham.requestFocus();
            return false;
        }
        if (gia.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập giáo viên chủ nhiệm", "Chưa nhập giáo viên chủ nhiệm", JOptionPane.WARNING_MESSAGE);
            txtGiaBan.requestFocus();
            return false;
        }
        if (loaisp.length() == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập giáo viên chủ nhiệm", "Chưa nhập giáo viên chủ nhiệm", JOptionPane.WARNING_MESSAGE);
            cbLoaiSP.requestFocus();
            return false;
        }
        return true;
    }
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String masp = txtMaSanPham.getText().trim(); // loại bỏ khoảng trắng không cần thiết từ đầu và cuối
        String tensp = txtTenSanPham.getText().trim();
        String gia = txtGiaBan.getText().trim();
        String loaisp = cbLoaiSP.getSelectedItem().toString().trim(); // 'Mũ'
        if (chucNangDaChon == ChucNang.ADD) {
            if (CheckInput() == false) {
                return;
            }
            if (DatabaseManager.Count("SANPHAM", "MASP", masp) > 0) {
                txtMaSanPham.requestFocus();
                JOptionPane.showMessageDialog(null, "Mã sản phẩm bạn nhập đã tồn tại trong csdl", "Trùng mã", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (DatabaseManager.AddProduct(masp, tensp, gia, loaisp)) {
                btnAdd.requestFocus();
                SwitchMode(ChucNang.NONE);
                ReloadTableProduct();
                JOptionPane.showMessageDialog(null, "Thêm thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                return;
            } else {
                JOptionPane.showMessageDialog(null, "Thêm thất bại", "Có lỗi xảy ra", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        if (chucNangDaChon == ChucNang.UPDATE) {
            if (CheckInput() == false) {
                return;
            }
            if (DatabaseManager.EditProduct(masp, tensp, gia, loaisp)) {

                btnSua.requestFocus();
                SwitchMode(ChucNang.NONE);
                ReloadTableProduct();
                JOptionPane.showMessageDialog(null, "Sửa thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Sửa thất bại", "Có lỗi ", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed
