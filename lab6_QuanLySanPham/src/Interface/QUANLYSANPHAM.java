    private void SwitchMode(int chucNang) {
        chucNangDaChon = chucNang;
        switch (chucNang) {
            // khi nhấn vào nút thêm
            case ChucNang.ADD: {
                boolean trangThai = true;
                txtMaSanPham.setEnabled(trangThai);
                txtTenSanPham.setEnabled(trangThai);
                txtGiaBan.setEnabled(trangThai);
                cbLoaiSP.setEnabled(trangThai);

                txtMaSanPham.requestFocus(); // làm nổi bật lên khi có sự tương tác từ chuột hoặc bàn phím
                btnSave.setEnabled(trangThai); // nút Lưu hiện lên
                btnSua.setEnabled(!trangThai); // nút sửa xám đi
                btnDelete.setEnabled(!trangThai); // nút xoá xám đi
                btnNotSave.setEnabled(trangThai); // nút K.Lưu hiện lên

                txtMaSanPham.setText("");
                txtTenSanPham.setText("");
                txtGiaBan.setText("");
                cbLoaiSP.setSelectedItem("");
                btnAdd.setText("Hủy"); // nút thêm sẽ thành nút huỷ
                break;
            }

            // khi nhấn vào nút lưu
            case ChucNang.UPDATE: {
                boolean trangThai = true;
                txtTenSanPham.setEnabled(trangThai);
                txtGiaBan.setEnabled(trangThai);
                cbLoaiSP.setEnabled(trangThai);

                txtTenSanPham.requestFocus();

                btnSave.setEnabled(trangThai);
                btnAdd.setEnabled(!trangThai);
                btnDelete.setEnabled(!trangThai);
                btnNotSave.setEnabled(trangThai); // nút K.Lưu hiện lên

                btnSua.setText("Hủy");
                break;
            }

            // khi nhấn vào nút huỷ
            case ChucNang.NONE: {
                boolean trangThai = false;
                txtMaSanPham.setEnabled(trangThai);
                txtTenSanPham.setEnabled(trangThai);
                txtGiaBan.setEnabled(trangThai);
                cbLoaiSP.setEnabled(trangThai);
                btnSave.setEnabled(trangThai);
                btnAdd.setEnabled(true);
                btnSua.setEnabled(true);
                btnDelete.setEnabled(true);
                btnAdd.setText("Thêm");
                btnSua.setText("Sửa");
            }
        }
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (chucNangDaChon == ChucNang.NONE)
            SwitchMode(ChucNang.ADD);
        else
            SwitchMode(ChucNang.NONE);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (tbl.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Chưa chọn sản phẩm để sửa", "Chưa chọn sản phẩm", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (chucNangDaChon == ChucNang.NONE)
            SwitchMode(ChucNang.UPDATE);
        else
            SwitchMode(ChucNang.NONE);
    }//GEN-LAST:event_btnSuaActionPerformed
    private void ReloadTableProduct() {
        if (DatabaseManager.ProductToTable(tbl) == false) {
            JOptionPane.showMessageDialog(null, "Lấy dữ liệu sản phẩm có lỗi", "Có lỗi xảy ra", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn sản phẩm nào để xóa", "Chưa chọn lớp", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int result = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa sản phẩm này không", "Xóa lớp?",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (result == JOptionPane.CANCEL_OPTION) {
            return;
        }
        String masp = (String) tbl.getValueAt(selectedRow, 0);
        if (DatabaseManager.DeleteProduct(masp)) {
            btnAdd.requestFocus();
            SwitchMode(ChucNang.NONE);
            ReloadTableProduct();
            JOptionPane.showMessageDialog(null, "Xóa thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Xóa thất bại", "Có lỗi xảy ra", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    void tbl_SelectionChanged() {
        int row = tbl.getSelectedRow();
        if (row >= 0) {

            String masp = (String) dfTableModel.getValueAt(row, 0);
            String tensp = (String) dfTableModel.getValueAt(row, 1);
            Double gia = (Double) dfTableModel.getValueAt(row, 2);

            txtMaSanPham.setText(masp.trim());
            txtTenSanPham.setText(tensp.trim());
            txtGiaBan.setText(gia.toString().trim());
//            cbLoaiSP.setSelectedItem(loaisp.trim());

        } else {
            txtMaSanPham.setText("");
            txtTenSanPham.setText("");
            txtGiaBan.setText("");
//            cbLoaiSP.setSelectedItem("");

        }
    }

